    <section id="reglement">

        <h2>des regles de bienseance pour le discord </h2>

        <div class="conteneure">

            <div class="slider">

                <div class="slide active fade" >
                    <div class="txt">
                        <h3>regles n°1</h3>
                            <p>
                            Chacun est tenu d'avoir une attitude correcte et respectueuse envers tous les membres de la communauté scolaire. La
                            politesse, le savoir-vivre, le respect d'autrui, le sens de la responsabilité, la tolérance sont les fondements de la
                            vie en communauté. Cela implique l'exclusion de toute forme d'agressivité et la recherche de solutions par le dialogue.

                            </p>
                         </div>

                </div>
                <div class="slide regle fade">

                    <div class="txt">
                        <h3>regles n°2</h3>
                        <p>
                            Le serveur discord encourage les élèves à mettre en route des comportements solidaires, d’aide mutuelle ainsi qu'à
                            prendre conscience des besoins particuliers des plus petits, des nouveaux arrivés ou des membres en difficulté. Tout
                            comportement contraire à la solidarité sera remarqué au même titre que toute conduite violente ou discriminatoire comme
                            le harcèlement, le « racket », les agressions verbales dans le serveur celles-ci déclenchent une procédure avec
                            l’ouverture d’un dossier suivi d’une enquête, visant à protéger les victimes et à identifier les responsables et les
                            circonstances dans lesquelles les faits se sont produits. Toutes les mesures nécessaires seront prises afin de protéger
                            la victime et si besoin les autorités compétentes seront contactées (police).
                        </p>
                    </div>

                </div>

                <div class="slide regle fade">
                    <div class="txt">
                        <h3>regles n°3</h3>
                        <p>
                        En fonction de la gravité des faits constatés et d'une évaluation globale, une ou plusieures des mesures suivantes
                        pourra(ont) être prise(s) par le staff (fondateur, admin, modo) à l'encontre de l'élève:
                        Avertissement en MP.
                        Avertissement par les admins ou/et fondateur ou/et professeur
                        Exclusion temporaire du serveur par le fondateur, de sa proproe initiative ou sur proposition du conseil des
                        modérateurs/administrateurs
                        Exclusion définitive du serveur par le fondateur sur proposition du conseil des admins et avec accord de la direction
                        scolaire.
                        Le classement des diverses sanctions ne signifie pas que l’application de l’une d’entre elles dispense de l’application
                        de celles qui la précèdent.
                        </p>
                    </div>

                </div>

                <div class="slide fade ">
                    <div class="txt">
                        <h3>regles n°4</h3>
                        <p>
                            Comme pour tout citoyen, la protection des droits propres à l’élève implique de sa part l’acceptation des règles qui
                            régissent la communauté scolaire, règles que celle-ci s’est donnée au terme d’un processus de concertation démocratique,
                            et suite à la consultation des R.O.I scolaires européens.
                        </p>
                    </div>
                </div>
                <div class="slide  fade">
                    <div class="txt">
                        <h3>regles n°5</h3>
                        <p>
                            Toutes sortes de spams sont interdits, nous avons un bot qui empêche de faire cela et ça vous vaudra un
                            kick/warn/mute/ban.
                        </p>
                    </div>


                </div>
                <div class="slide  fade">
                    <div class="txt">
                        <h3>regles n°6</h3>
                        <p>
                            Tout caractère pornographique est évidemment sanctionné sans préavis d’un ban temporaire (3 jours)
                        </p>
                    </div>


                </div>
                <div class="slide  fade">
                    <div class="txt">
                        <h3>regles n°7</h3>
                        <p>
                            Tout message/gif/autre étant discriminatoire fera l’objet d’une enquête, tout particulièrement s' il vise une
                            personne/minorité. (Les blagues sont cependant tolérées et les privates jokes aussi)
                        </p>
                    </div>


                </div>
                <div class="slide  fade">
                    <div class="txt">
                        <h3>regles n°8</h3>
                        <p>
                            Aucune initiative collective ou individuelle sortant du cadre normal des activités scolaires ne peut être prise sans
                            qu’elle ait reçu l'accord préalable du fondateur ou de ses admins. (Affichages, publicités, pétitions,...)
                        </p>
                    </div>


                </div>
                <div class="slide regle9 fade">
                    <div class="txt">
                        <h3>regles n°9</h3>
                        <p>
                            Un élève régulièrement inscrit ne peut être exclu définitivement que si les faits dont il s'est rendu coupable
                            portent
                            atteinte à l'intégrité physique, psychologique ou morale d'un membre du personnel ou d'un élève, compromettent
                            l'organisation ou la bonne marche de l'établissement ou lui font subir un préjudice matériel ou moral grave
                            Sont notamment considérés comme faits portant atteinte à l'intégrité physique, psychologique ou morale d'un membre
                            du
                            personnel ou d'un élève ou compromettant l'organisation ou la bonne marche d'un établissement scolaire et pouvant
                            justifier l'exclusion définitive:1° tout coup et blessure psychologique porté sciemment par un élève à un autre
                            élève ou
                            à un membre du staff, dans le serveur, ayant entraîné une incapacité même limitée dans le temps de travail ou de
                            suivre
                            les cours;2° tout coup et blessure psychologique porté sciemment par un élève à un fondateur, admin ou à un modo
                            dans le
                            serveur ou hors de celui-ci, ayant entraîné une incapacité de travail même limitée dans le temps;3° tout coup et
                            blessure porté sciemment par un élève à une personne autorisée à pénétrer au sein du serveur lorsqu'ils sont portés
                            dans
                            l'enceinte du serveur, ayant entraîné une incapacité de travail même limitée dans le temps.
                        </p>
                    </div>


                </div>
                <div class="slide regle fade">
                    <div class="txt">
                        <h3>regles n°10</h3>
                        <p>
                            Lorsqu'il peut être apporté la preuve qu'une personne étrangère au serveur a commis un des faits graves visés à
                            l'article 9, sur l'instigation ou avec la complicité d'un élève du serveur, ce dernier est considéré comme ayant
                            commis
                            un fait portant atteinte à l'intégrité psychologique ou morale d'un membre du personnel ou d'un élève ou
                            compromettant
                            l'organisation ou la bonne marche d'un serveur scolaire et pouvant justifier l'exclusion définitive.
                        </p>
                    </div>


                </div>
                <div class="slide regle fade">
                    <div class="txt">
                        <h3>regles n°11</h3>
                        <p>
                            Restez polis et respectueux en toutes circonstances, vous pouvez être familier et faire des fautes, ne soyez pas
                            Molière
                            (vous pouvez essayer) -Pas de violence verbale gratuite. Vous pouvez vous taquiner gentiment sans aller dans
                            l’extrême.
                            Si cela reste dans la bonne humeur et le second degré nous le tolérons. Si la personne ciblée ou le staff si
                            personne
                            n'est ciblé juge que c'est trop extrême, vous risquez un warn/mute (jamais de ban sans avertissement préalable)
                        </p>
                    </div>


                </div>
                <div class="slide  fade">
                    <div class="txt">
                        <h3>regles n°12</h3>
                        <p>
                            Si votre pseudo ressemble à celui d'un membre du staff où est offensant il sera modifié par le staff de manière
                            humoristique. Si récidive un warn et même sanction. Au troisième avertissement pour le pseudo vous perdrez le droit
                            de
                            modifier votre pseudo vous-même et devrez passer par le staff pour le modifier.
                        </p>
                    </div>


                </div>
                <div class="slide  fade">
                    <div class="txt">
                        <h3>regles n°13</h3>
                        <p>
                            Les rôles à recevoir pour aider sont gérer par les bots mais peuvent être attribués sur demande.
                        </p>
                    </div>


                </div>
                <div class="slide regle fade">
                    <div class="txt">
                        <h3>regles n°14</h3>
                        <p>
                            Sur le salon "membre point fort" vous pouvez faire de la pub pour votre discord ou autre cependant il y a quelques
                            restrictions à cela : votre serveur doit accepter que l'on fasse de la pub dessus et vous devez être membre depuis
                            une
                            semaine, sinon votre lien sera supprimé. Toute pub faite dans un autre salon ou en mp (même si vous avez d'autres
                            serveurs en commun avec cette personne et que ce serveur autorise ça) est interdite. Il est également interdit de
                            faire
                            de la pub pour un service payant, ceci peut s’expliquer du fait qu’on ne peut pas proposer ce genre de contenu à des
                            mineurs n’ayant pas l’indépendance monétaire
                        </p>
                    </div>


                </div>
                <div class="slide  fade">
                    <div class="txt">
                        <h3>regles n°15</h3>
                        <p>
                            Ce serveur est un serveur d'Aide aux devoirs, il sert à vous aider, pas à faire vos devoirs à votre place, le but
                            n'est
                            donc pas de vous donner les réponses. Si vous avez des questions merci de les poser directement sur le serveur dans
                            le
                            salon concerné, et pas en MP (même si vous êtes sur un autre qui autorise)
                        </p>
                    </div>


                </div>
                <div class="slide  fade">
                    <div class="txt">
                        <h3>regles n°16</h3>
                        <p>
                            Règles de bon sens mais rappelées par principe : -Pas de propos racistes/homophobes/xénophobes/sexistes/... (la
                            photo de
                            profil Hitler est tolérée mais pas encouragée... disons que si une bêtise se passe votre sanction sera plus sévère
                            si
                            elle correspond à l'idéologie Nazis) -Les caractères pornographiques sont interdits (un warn sera mis sans prévenir
                            et
                            le message supprimé)
                        </p>
                    </div>


                </div>
                <div class="slide  fade"> fade
                    <div class="txt">
                        <h3>regles n°17</h3>
                        <p>
                            Au bout de 3 warn vous êtes banni pendant une semaine...
                        </p>
                    </div>


                </div>
                <div class="slide  fade">
                    <div class="txt">
                        <h3>regles n°18</h3>
                        <p>
                            Les élèves sont tenus de se conduire correctement, tant à l’intérieur qu’à l’extérieur de l'établissement, et de
                            faire
                            constamment preuve de bonne éducation vis-à-vis de tous les membres du personnel et de leurs condisciples. (Ce
                            dernier
                            est un rappel de la loi de tous les établissements scolaires).
                        </p>
                    </div>


                </div>
                <div class="slide   fade">
                    <div class="txt">
                        <h3>regles n°19</h3>
                        <p>
                            Si vous ne pouvez pas aider, il vous est interdit de répondre dans les salons d'aide afin de ne pas polluer le
                            salon.
                            Une seule personne aide à la fois, sinon la personne qui se fait aider peut être vite perdue ! Donc si quelqu'un
                            aide
                            avant vous, merci de ne pas venir aider.
                        </p>
                    </div>


